from RCAIDE.Library.Components import Component   
from RCAIDE.Library.Methods.Energy.Thermal_Management.Batteries.Heat_Acquisition_Systems.No_Heat_Acquisition import no_heat_acquisition_model

# ----------------------------------------------------------------------------------------------------------------------
#  No_Heat_Exchanger
